public class GaltoLit{
	public static void main(String[] args){
		//Create a gallon to liter conversion


		double gallons;		//holds gallons
		double liters;		//holds liters

		liters = 10000;	//liters

		gallons = liters / 3.7854; // conversion to gallons

		System.out.println(liters + " liters in " + gallons + " gallons.");



	}
}